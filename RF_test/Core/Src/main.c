/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_LPUART1_UART_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Initialize leds */
  BSP_LED_Init(LED_BLUE);
  BSP_LED_Init(LED_GREEN);
  BSP_LED_Init(LED_RED);

  /* Initialize USER push-button, will be used to trigger an interrupt each time it's pressed.*/
  BSP_PB_Init(BUTTON_SW1, BUTTON_MODE_EXTI);
  BSP_PB_Init(BUTTON_SW2, BUTTON_MODE_EXTI);
  BSP_PB_Init(BUTTON_SW3, BUTTON_MODE_EXTI);

  /* Initialize COM1 port (115200, 8 bits (7-bit data + 1 stop bit), no parity */
  BspCOMInit.BaudRate   = 115200;
  BspCOMInit.WordLength = COM_WORDLENGTH_8B;
  BspCOMInit.StopBits   = COM_STOPBITS_1;
  BspCOMInit.Parity     = COM_PARITY_NONE;
  BspCOMInit.HwFlowCtl  = COM_HWCONTROL_NONE;
  if (BSP_COM_Init(COM1, &BspCOMInit) != BSP_ERROR_NONE)
  {
    Error_Handler();
  }

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  /* Infinite loop */
  while (1) {
      if (adc_conversion_complete) {
          adc_conversion_complete = 0;

          /* Process ADC data */
          for (int i = 0; i < FFT_SIZE; i++) {
              fft_input_I[i] = ((float32_t)adc_buffer[i * NUM_CHANNELS]) * (3.3f / 4095.0f); // Normalize I channel
              fft_input_Q[i] = ((float32_t)adc_buffer[i * NUM_CHANNELS + 1]) * (3.3f / 4095.0f); // Normalize Q channel
          }

          /* Initialize FFT instance */
          arm_rfft_fast_instance_f32 fft_instance;
          arm_rfft_fast_init_f32(&fft_instance, FFT_SIZE);

          /* Perform FFT */
          arm_rfft_fast_f32(&fft_instance, fft_input_I, fft_output_I, 0);
          arm_rfft_fast_f32(&fft_instance, fft_input_Q, fft_output_Q, 0);

          /* Calculate magnitudes */
          float32_t fft_magnitude[FFT_SIZE / 2];
          for (int i = 0; i < FFT_SIZE / 2; i++) {
              float32_t real = fft_output_I[2 * i];
              float32_t imag = fft_output_Q[2 * i];
              fft_magnitude[i] = sqrtf(real * real + imag * imag);
          }

          /* Find peak frequency */
          uint32_t maxIndex = 0;
          float32_t maxValue = 0;
          arm_max_f32(fft_magnitude, FFT_SIZE / 2, &maxValue, &maxIndex);

          /* Calculate frequency and speed */
          float32_t frequency = ((float32_t)maxIndex * SamplingRate) / FFT_SIZE;
          float32_t speed_mps = (frequency * Wavelength) / 2.0f; // Doppler effect
          float32_t speed_mph = speed_mps * 2.23694f; // Convert to mph

          /* Log speed */
          LogSpeed(speed_mph);

          /* Restart ADC DMA */
          HAL_ADC_Start_DMA(&hadc1, (uint32_t *)adc_buffer, ADC_BUF_LEN);
      }

      /* Blink LED to indicate system is running */
      HAL_GPIO_TogglePin(LED_GREEN_GPIO_Port, LED_GREEN_Pin);
      HAL_Delay(500);
  }

  /* USER CODE END 3 */
}

